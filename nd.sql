DROP DATABASE IF EXISTS bookstore;
CREATE DATABASE bookstore;
USE bookstore;

CREATE TABLE books(
	id INT(11) NOT NULL AUTO_INCREMENT,
    title VARCHAR(20) NOT NULL,
    year CHAR(5) NOT NULL,
    pages VARCHAR(20) NOT NULL,
    PRIMARY KEY(id)
);
